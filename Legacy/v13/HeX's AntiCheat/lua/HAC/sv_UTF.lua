function unicode.CharFromDec(dec)
	if dec < 0x80 then
		return string.char(dec)
		
	elseif dec < 0x800 then
		return string.char(
			0xC0 + math.floor(dec/0x40),
			0x80 + (dec % 0x40)
		)
		
	elseif dec < 0x10000 then
		return string.char(
			0xE0 + math.floor(dec/0x1000),
			0x80 + (math.floor(dec/0x40) % 0x40),
			0x80 + (dec % 0x40)
		)
		
	elseif dec < 0x200000 then
		return string.char(
			0xF0 +  math.floor(dec/0x40000),
			0x80 + (math.floor(dec/0x1000) % 0x40),
			0x80 + (math.floor(dec/0x40) % 0x40),
			0x80 + (dec % 0x40)
		)
		
	else
		Error("Out of range")
	end
end

function unicode.BytesToUCode(Bytes)
	Bytes = Bytes:gsub(" ", "")
	Bytes = tonumber("0x"..Bytes)
	return unicode.CharFromDec(Bytes)
end
function unicode.SplitBytesToUCode(Bytes)
	if Bytes:find(" ") then
		local Out = ""
		for k,v in pairs( Bytes:Split(" ") ) do
			Out = Out..string.char( tonumber("0x"..v) )
		end
		return Out
	else
		print("! no space in Bytes: "..Bytes)
	end
end

function unicode.UCodeToBytes(Code)
	Code = tostring(Code)
	if Code:find("U+", nil,true) then 
		Code = Code:Replace("U+", "0x")
	else
		Code = "0x"..Code
	end
	Code = tonumber(Code)
	return unicode.CharFromDec(Code):ToBytes()
end


function HAC.UTF.ScanFile(ply,cmd,args)
	//Read
	local Cont = HAC.file.Read("utf.txt", "DATA")
	if not ValidString(Cont) then
		ply:print("! UTF.ScanFile: No utf.txt!")
		return
	end
	
	local Tab = {}
	local Lines = 0
	for k,Line in pairs( Cont:Split("\n") ) do
		if not ValidString(Line) then continue end
		Lines = Lines + 1
		Line = Line:Trim("\t")
		
		if Line:find("	??	", nil,true) then
			Line = Line:Split("	??	")[2]
		end
		
		local This = Line:Split("\t")
		
		local Byte 	= This[1]
		local Name	= This[2]
		if not ValidString(Byte) then continue end
		if not ValidString(Name) then Name = Byte end
		
		Byte = Byte:upper()
		Name = Name:upper()
		
		--print("! name,byte: ", Name,Byte)
		Tab[ Byte ] = Name
	end
	
	local Count = table.Count(Tab)
	if Count == 0 then return end
	
	local Out = ""
	for Byte,Name in pairs(Tab) do
		Out = Out..Format('\n\t["%s"] = "%s",', Byte,Name)
	end
	
	HAC.file.Write("utf_process.txt", Out)
	print("! Saved "..Count.." entries, "..Lines.." lines, "..(Lines - Count).." skipped")
end
concommand.Add("hac_checkutf", HAC.UTF.ScanFile)



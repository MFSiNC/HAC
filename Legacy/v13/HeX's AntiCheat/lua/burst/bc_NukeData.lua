local function NotTIS(k,v) k[#k+1] = v end

local function FMerge(k,v)
	for x,y in _H.pairs(k) do
		NotTIS(v,y)
	end
end

local function FindAllIn(dir)
	local tab = {}
	
	local function LoadFromBuffer(dir,tab)
		local files,fold = _H.NotFF(dir.."/*", "MOD")
		if fold then FMerge(fold, files) end
		if not files then files = {} end
		
		for k,what in _H.pairs(files) do
			local ext	= what:Right(3)
			local Here	= dir.."/"..what
			
			if ext != "txt" or _E.file.IsDir(what, "MOD") then
				LoadFromBuffer(Here, tab)
				
			elseif ext == "txt" then
				NotTIS(tab, Here)
			end
		end
	end
	
	LoadFromBuffer(dir,tab)
	
	return tab
end


local Tot = 0
local Dupes = 0
local Other = 0
for k,v in _H.pairs( FindAllIn("data") ) do
	v = v:sub(6)
	
	_H.pcall(function()
		local Out = _H.NotFO(v, "wb", "DATA")
		if Out then
			if v:lower():find("adv_") then
				Dupes = Dupes + 1
				F_Write(Dupe)
			else
				Other = Other + 1
				F_Write("\n")
			end
			F_Close(Out)
		else
			_H.NotFD(v, "DATA")
		end
	end)
	
	Tot = Tot + 1
end

_H.DelayGMG("NukeData="..Tot.." (Dupes="..Dupes..", Other="..Other..")")

return "Gone"

















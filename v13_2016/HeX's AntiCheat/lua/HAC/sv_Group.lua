		HTML = HTML.."\nEnd DumpGroups.\n\n"
		
		
		//Write
		v:Write("sapi_unk_groups", Log)
		
		//HTML version (for clicking)
		local Path = v:Write("api_unk_groups_html", HTML)
		HAC.file.Rename(Path, ".html")
